package javaBasics;

public class DataTypes {

	public static void main(String[] args) {
		// Data types and memory size of data types
		
		//Primitive Data types: byte, int, short, long, float, double, char, boolean
		//Non-Primitive Data types: String Array
		
		//Primitive Data types
		
//integer format	
		
		byte a=10;     //byte size is 1 byte and range is -128 to 127
		short i=550;   //short size is 2 bytes and range is -32,768 to 32,767
		int j=349;     //int size is 4 bytes and range is -2,147,483,648 to 2,147,483, 647
		long l= -3535; //long size is 8 bytes
		
		
//Decimal number format
		
		float d= 50900;     // size is 4 bytes and range is like 6-7 significant decimal digits
		double d1= 12.33;   // size is 8 bytes and range is like 15 significant decimal digits

//Char format
		
		char c1='a';     //size 2 bytes 
		System.out.println((int)c1);
		
		
		
		
		
		
		
	}

}
